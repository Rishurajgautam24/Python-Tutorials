# Escape Sequences
print("Hello\" world")
print('Hello\' world')
print('Hello\n world')
print('Hello\t world')
print('Helloo\b\b world')

# Escape sequences are used to represent certain special characters within strings.

# The following table lists the escape sequences that can be used in Python:

# Escape Sequence	Description

# \'	            Single quote (')

# \"	            Double quote (")

# \n	            Newline

# \t	            Horizontal tab

# \b	            Backspace

