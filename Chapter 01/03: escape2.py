# Escape Charatcer as Normal Text
print("Hello \" world")

# \\ Backslash (\)

print("Hello \\ world")

# print("Hello \\" world")



# \\ is used to represent a single backslash character within a string.
# For example:
print("Hello \\\" world")


# Triple Backslash
# \\\' and \\\"

print('Hello \\\' world')

