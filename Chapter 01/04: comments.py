# Comments in Python

# Comments are used to document code and improve its readability. They are ignored by the Python interpreter and are not executed. Comments can be used to explain the purpose of code, provide context, or disable certain parts of the code.

# Single-line comments start with a hash (#) symbol and continue until the end of the line. For example:

print("Hello world")

# Multi-line comments can be created by enclosing the text within triple quotes (''' or """). This is useful for longer comments or docstrings. For example:

"""
Comments are used to document code and improve its readability. They are ignored by the Python interpreter and are not executed. Comments can be used to explain the purpose of code, provide context, or disable certain parts of the code.
"""

# Comments can also be used to disable certain parts of the code temporarily. This can be useful for debugging or testing different sections of the code. For example:

# This block of code prints hello world in terminal
print("Hello, World!")
# print("This Above program is commented out.")

# In this example, the second print statement is commented out, so it will not be executed when the code is run.

# Comments are an essential part of writing clean and maintainable code. They help other developers understand your code and make it easier to collaborate on projects. It is good practice to include comments in your code to explain complex logic, algorithms, or any other important details.

