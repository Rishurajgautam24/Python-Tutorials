# Strings 
# Strings are a sequence of characters inside single or double quotes

f_name = "Rishu"
l_name = 'Raj'


#Concatenation ( adding two strings )
full_name = f_name +" "+ l_name
print(full_name)


# Can't add number with String
# print(f_name + 3)
# String can only be added with a string
print(f_name + "3")

#  str() Function
print(f_name +" "+ str(34))


#  Multiply in String
print((f_name+" ")  * 3)